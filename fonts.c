//    } else if (font_id == 24) {
//        f_latin = &Ubuntu_L_24;
//        f_cyrillic = &Ubuntu_C_24;
//    }
    y += (f_latin->yAdvance * 3 / 4);
    while (*str) {
        uint32_t code = decode_utf8(&str);
        const GFXfont *current_font = NULL;
        if (code >= 32 && code <= 127) {
            current_font = f_latin;
        } else if (code >= 1025 && code <= 1105) {
            current_font = f_cyrillic;
        }
        if (current_font) {
            draw_gfx_char(self, x, y, (uint16_t)code, color, current_font);
            x += current_font->glyph[code - current_font->first].xAdvance;
        } else {
            x += (font_id / 2);
        }
        if (x >= self->width) break;
    }
